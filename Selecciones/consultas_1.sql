--PRODUCTOS
SELECT
	*
FROM
	PRODUCTOS
WHERE
	NOMBRE LIKE 'Q%';

SELECT
	*
FROM
	PRODUCTOS
WHERE
	DESCRIPCION IS NULL;

SELECT
	*
FROM
	PRODUCTOS
WHERE
	PRECIO BETWEEN MONEY (2) AND MONEY  (3);

--CUENTAS
SELECT
	NUMERO_CUENTA,
	SALDO
FROM
	CUENTAS;

SELECT
	*
FROM
	CUENTAS
WHERE
	FECHA_CREACION BETWEEN CURRENT_DATE - INTERVAL '2 MONTH' AND CURRENT_DATE;

SELECT
	NUMERO_CUENTA,
	SALDO
FROM
	CUENTAS
WHERE
	FECHA_CREACION BETWEEN CURRENT_DATE - INTERVAL '2 MONTH' AND CURRENT_DATE;

--ESTUDIANTES
SELECT
	NOMBRE,
	CEDULA
FROM
	ESTUDIANTES;

SELECT
	NOMBRE
FROM
	ESTUDIANTES
WHERE
	CEDULA LIKE '17%';

SELECT
	NOMBRE || ' ' || APELLIDO AS "nombre completo"
FROM
	ESTUDIANTES
WHERE
	NOMBRE LIKE 'A%';

--REGISTRO_ENTRADA
SELECT
	CEDULA_EMPLEADO,
	FECHA,
	HORA
FROM
	REGISTROS_ENTRADA;

SELECT
	*
FROM
	REGISTROS_ENTRADA
WHERE
	HORA BETWEEN '7:00' AND '14:00';

SELECT
	*
FROM
	REGISTROS_ENTRADA
WHERE
	HORA > '08:00';

--VIDEOJUEGOS
SELECT
	*
FROM
	VIDEOJUEGOS
WHERE
	NOMBRE LIKE 'C%';

SELECT
	*
FROM
	VIDEOJUEGOS
WHERE
	VALORACION BETWEEN 9 AND 10;

SELECT
	*
FROM
	VIDEOJUEGOS
WHERE
	DESCRIPCION IS NULL;

--TRANSACCIONES
SELECT
	*
FROM
	TRANSACCIONES
WHERE
	TIPO = 'D';

SELECT
	*
FROM
	TRANSACCIONES
WHERE
	MONTO BETWEEN MONEY (200) AND MONEY  (2000);

SELECT
	CODIGO,
	MONTO,
	TIPO,
	FECHA
FROM
	TRANSACCIONES
WHERE
	FECHA IS NOT NULL;