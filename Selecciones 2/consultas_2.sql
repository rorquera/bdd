--PRODUCTOS
SELECT
	*
FROM
	PRODUCTOS
WHERE
	STOCK = 10
	AND PRECIO < MONEY (10);

SELECT
	NOMBRE,
	STOCK
FROM
	PRODUCTOS
WHERE
	NOMBRE LIKE '%m%'
	OR STRPOS(DESCRIPCION, ' ') > 0;

SELECT
	NOMBRE
FROM
	PRODUCTOS
WHERE
	DESCRIPCION IS NULL
	OR STOCK = 0;

--CUENTAS
SELECT
	NUMERO_CUENTA,
	SALDO
FROM
	CUENTAS
WHERE
	SALDO > MONEY (100)
	AND SALDO < MONEY (1000);

SELECT
	*
FROM
	CUENTAS
WHERE
	FECHA_CREACION BETWEEN CURRENT_DATE - INTERVAL '1 YEAR' AND CURRENT_DATE;

SELECT
	*
FROM
	CUENTAS
WHERE
	SALDO = MONEY (0)
	OR CEDULA_PROPIETARIO LIKE '%2';

--ESTUDIANTES
SELECT
	NOMBRE,
	APELLIDO
FROM
	ESTUDIANTES
WHERE
	NOMBRE LIKE 'M%'
	OR APELLIDO LIKE '%Z';

SELECT
	NOMBRE
FROM
	ESTUDIANTES
WHERE
	CEDULA LIKE '%32%'
	AND CEDULA LIKE '18%';

SELECT
	NOMBRE || ' ' || APELLIDO AS "nombre completo"
FROM
	ESTUDIANTES
WHERE
	CEDULA LIKE '%6'
	OR CEDULA LIKE '17%';

--REGISTROS_ENTRADAS
SELECT
	*
FROM
	REGISTROS_ENTRADA
WHERE
	EXTRACT(
		MONTH
		FROM
			FECHA
	) = 9
	OR CEDULA_EMPLEADO LIKE '17%';

SELECT
	*
FROM
	REGISTROS_ENTRADA
WHERE
	EXTRACT(
		MONTH
		FROM
			FECHA
	) = 8
	AND (CEDULA_EMPLEADO LIKE '17%')
	AND (HORA BETWEEN '8:00' AND '12:00');

SELECT
	*
FROM
	REGISTROS_ENTRADA
WHERE
	(
		EXTRACT(
			MONTH
			FROM
				FECHA
		) = 8
		AND (CEDULA_EMPLEADO LIKE '17%')
		AND (HORA BETWEEN '8:00' AND '12:00')
	)
	OR (
		EXTRACT(
			MONTH
			FROM
				FECHA
		) = 9
		AND (CEDULA_EMPLEADO LIKE '08%')
		AND (HORA BETWEEN '9:00' AND '13:00')
	);

--VIDEOJUEGOS
SELECT
	*
FROM
	VIDEOJUEGOS
WHERE
	NOMBRE LIKE '%C%'
	OR VALORACION = 7;

SELECT
	*
FROM
	VIDEOJUEGOS
WHERE
	(CODIGO BETWEEN 3 AND 7)
	OR VALORACION = 7;

SELECT
	*
FROM
	VIDEOJUEGOS
WHERE
	(
		DESCRIPCION LIKE '%guerra%'
		AND VALORACION > 7
		AND NOMBRE LIKE 'C%'
	)
	OR (
		VALORACION > 8
		AND NOMBRE LIKE 'D%'
	);

--TRANSACCIONES
SELECT
	*
FROM
	TRANSACCIONES
WHERE
	TIPO = 'C'
	AND CAST(NUMERO_CUENTA AS INTEGER) BETWEEN 22004 AND 222001;

SELECT
	*
FROM
	TRANSACCIONES
WHERE
	TIPO = 'D'
	AND EXTRACT(
		DAY
		FROM
			FECHA
	) = 25
	AND EXTRACT(
		MONTH
		FROM
			FECHA
	) = 5
	AND CAST(NUMERO_CUENTA AS INTEGER) BETWEEN 22007 AND 22010;

SELECT
	*
FROM
	TRANSACCIONES
WHERE
	CODIGO BETWEEN 1 AND 5
	AND CAST(NUMERO_CUENTA AS INTEGER) BETWEEN 22002 AND 22004
	AND (
		(
			EXTRACT(
				DAY
				FROM
					FECHA
			) BETWEEN 26 AND 29
		)
		AND EXTRACT(
			MONTH
			FROM
				FECHA
		) = 5
	);